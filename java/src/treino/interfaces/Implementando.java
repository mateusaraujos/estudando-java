package treino.interfaces;

public class Implementando implements Interface {
    public void falar() {
        System.out.println("Falar com o pessoal.");
    }

    public void andar() {
        System.out.println("Andar com o pessoal.");
    }

    public void pegar() {
        System.out.println("Pegar uma maça e um copo de suco.");
    }

    public void comer() {
        System.out.println("Comer a maça.");
    }

    public void beber() {
        System.out.println("Beber o suco.");
    }
}
